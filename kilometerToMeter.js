// making a function to make kilometer to meter converter using javascript;
function kilometerToMeter(kilometer){
        var meter = kilometer * 1000;
        return meter;
}
var firstMeter= kilometerToMeter(10); //checking output one;
console.log(firstMeter);
var secondtMeter= kilometerToMeter(2); //checking output two;
console.log(secondtMeter);
var thirdtMeter= kilometerToMeter(20); //checking output three;
console.log(thirdtMeter);
var fourthtMeter= kilometerToMeter(17); //checking output four;
console.log(fourthtMeter);
